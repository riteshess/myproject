    protected Object callStoredFunction(String stmt, Object[] bindVars) {

        CallableStatement st = null;
        try {
            /** 1. Create a JDBC CallabledStatement */
            st = getDBTransaction().createCallableStatement("begin ? := " + stmt + ";end;", 0);
            /** 2. Register the first bind variable for the return value. and last 2 variable for output variable of function. */
            st.registerOutParameter(1, NUMBER);
            st.registerOutParameter(6, NUMBER);
            st.registerOutParameter(7, NUMBER);
            if (bindVars != null) {
                /** 3. Loop over values for the bind variables passed in, if any */
                for (int z = 0; z < bindVars.length; z++) {
                    /** 4. Set the value of user-supplied bind vars in the stmt */
                    st.setObject(z + 2, bindVars[z]);
                }
            }
            /** 5. Set the value of user-supplied bind vars in the stmt */
            st.executeUpdate();
            /** 6. set value of eoid and eomstid */
            setTvouEoId(Integer.parseInt(st.getObject(6).toString()));
            setTvouEomstId(Integer.parseInt(st.getObject(7).toString()));
            return null;
        } catch (SQLException e) {
            int end = e.getMessage().indexOf("\n");

            throw new JboException(e.getMessage().substring(11, end));
        } finally {
            if (st != null) {
                try {
                    /** 7. Close the statement */
                    st.close();
                } catch (SQLException e) {
                    throw new JboException(e.getMessage());
                }
            }
        }
    }
